// src/prompts.js — 프롬프트 조립 (프롬프트_템플릿.md 기준, 프롬프트 문자열은 이 파일에만 둔다)
//
// 주의: 자막(TRANSCRIPT)은 여기서 프롬프트에 넣지 않는다.
// anthropic.js가 자막을 별도 캐시 블록으로 삽입하므로, 이 파일의 프롬프트들은
// "위 [강의 자막]을 바탕으로..." 형태로 자막 블록을 참조만 한다.

/** 공통 헤더 — 모든 생성 호출의 system 프롬프트 (동일 유지 = 프롬프트 캐시 프리픽스 보존) */
export const COMMON_HEADER = `당신은 소잉 강사 '자야쌤'의 콘텐츠 작가다.
[철학] "옷을 만드는 게 아니라 짓는다." 왜 그렇게 하는지까지 담는다.
[대상] 제대로 만들고 싶은 30~50대 취미 소잉러.
[원칙] 자막에 없는 사실은 지어내지 않는다.`;

const TYPE_LABELS = {
  cafe: '네이버 카페 서머리',
  caption: '인스타 캡션',
  carousel: '인스타 캐러셀 기획',
  capture: '캡쳐 가이드',
};

function topicLine(topic) {
  return topic ? `[강의 주제] ${topic}\n` : '';
}

/** 보이스 프로파일 JSON → 프롬프트용 요약 문자열 (대표 캡션은 EXAMPLES로 분리) */
function voiceSummary(voice) {
  const { representative_captions, ...rest } = voice || {};
  return JSON.stringify(rest, null, 2);
}

/** 대표 캡션 원문 3~5개 */
function voiceExamples(voice) {
  const caps = (voice?.representative_captions || []).slice(0, 5);
  if (caps.length === 0) return '(대표 캡션 없음)';
  return caps.map((c, i) => `--- 예시 ${i + 1} ---\n${c}`).join('\n\n');
}

/** 말투 지침 블록 (cafe·caption 공용) */
function voiceBlock(voice) {
  return `[말투 지침] 아래 보이스 프로파일과 예시 캡션의 목소리를 그대로 재현한다.
보이스:
${voiceSummary(voice)}

예시 캡션:
${voiceExamples(voice)}`;
}

// ─────────────────────────────────────────────
// 1. 말투 분석 → 보이스 프로파일 (learn-voice)
// ─────────────────────────────────────────────
export function buildVoicePrompt(captions) {
  const joined = captions.map((c, i) => `--- 캡션 ${i + 1} ---\n${c}`).join('\n\n');
  return `아래는 자야쌤이 직접 쓴 인스타그램 캡션 모음이다. 이 사람의 문체를 분석해
다른 글도 같은 목소리로 쓸 수 있도록 '보이스 프로파일'을 만들어라.

반드시 아래 JSON 형식으로만 출력한다(설명·머리말 없이):
{
  "tone": "전반적 어조 2~3문장",
  "sentence_style": "문장 길이·리듬·끝맺음 패턴",
  "signature_expressions": ["자주 쓰는 표현/어미/입버릇 5~10개"],
  "punctuation_emoji": "말줄임표·기호·이모지 사용 습관",
  "hook_patterns": ["첫 문장을 여는 방식 3~5개"],
  "avoid": ["이 사람이 쓰지 않을 표현/톤"],
  "representative_captions": ["가장 대표적인 원문 캡션 3~5개 그대로"]
}

[캡션 모음]
${joined}`;
}

// ─────────────────────────────────────────────
// 2. 네이버 카페 서머리 (voice 적용)
// ─────────────────────────────────────────────
export function buildCafePrompt({ voice, topic }) {
  return `${voiceBlock(voice)}

[티저 원칙] 이 글의 목적은 강의 내용을 '설명'하는 것이 아니라, 읽는 사람이 '이 강의를 꼭 들어야겠다'는 욕구를 느끼게 하는 것이다.
- 독자가 공감할 만한 흔한 실수·고민을 구체적으로 짚는다 (답은 주지 않는다).
- 핵심 노하우는 "이 부분은 강의에서 자야쌤이 정말 자세하게 풀어줘요" 식으로 힌트만 준다.
- 강의를 보지 않으면 알 수 없는 포인트를 2~3개 질문 형태로 던진다 (예: "왜 다림질을 이 순서로 해야 할까요?").
- 완전 공개 금지: 구체적 수치, 단계별 방법, 해결책을 직접 쓰지 않는다.
- 마무리는 수강 또는 다음 강의 기대를 유도하는 CTA로 끝낸다.

[작업] 위 [강의 자막]을 바탕으로 네이버 카페에 바로 올릴 강의 서머리를 쓴다.
구성: 따뜻한 인사 + 독자 공감(고민/실수) / 이번 강의에서 다루는 핵심 질문 2~3개(답 없이) / 강의 속 인상적인 장면·노하우 힌트(내용은 가리고 분위기만) / 수강 CTA.
**글자수는 2,000자 내외**로 맞춘다. 완성된 게시글 본문만 출력한다.
${topicLine(topic)}`;
}

// ─────────────────────────────────────────────
// 3. 캡쳐 가이드 (타임스탬프 기반 — 원본 자막 사용)
// ─────────────────────────────────────────────
export function buildCapturePrompt() {
  return `[작업] 위 [강의 자막]을 바탕으로 카페 글과 카드뉴스에 넣을 캡쳐 시점을 5~8개 추천한다.
각 줄 형식: [MM:SS] — 어떤 장면인지 / 왜 이 컷이 중요한지
반드시 자막에 실제로 등장한 타임스탬프만 사용한다. 목록만 출력한다.`;
}

// ─────────────────────────────────────────────
// 4. 인스타 캐러셀 기획 (8~10컷)
// ─────────────────────────────────────────────
export function buildCarouselPrompt({ topic }) {
  return `[작업] 위 [강의 자막]을 바탕으로 8~10컷 인스타 캐러셀을 기획한다.
후크 구조: 좌절/실패로 시작 → 공감 → 자야쌤의 해법(전문성) → CTA(공구/수강).
디자인 톤(참고): 배경 베이지 #fff4e1, 포인트 진네이비 #18284e, 모던·깔끔·노블, 실사/사실적.
캡쳐 배경은 자막의 실제 타임스탬프만 사용한다(캡쳐 가이드와 일관성 유지).
각 컷을 아래 형식으로 출력한다:
CUT n | 헤드라인(임팩트 한 줄) | 서브(짧게) | 배경(실사캡쳐[MM:SS] 또는 디자인 지시)
${topicLine(topic)}`;
}

// ─────────────────────────────────────────────
// 5. 인스타 캡션 (voice 적용)
// ─────────────────────────────────────────────
export function buildCaptionPrompt({ voice, topic }) {
  return `${voiceBlock(voice)}

[티저 원칙] 이 캡션의 목적은 강의 내용을 '설명'하는 것이 아니라, 읽는 사람이 '이 강의를 꼭 봐야겠다'는 욕구를 느끼게 하는 것이다.
- 첫 줄: 독자가 겪어봤을 실패·좌절 장면을 구체적으로 묘사한다 (예: "왜 내가 하면 맨날 이렇게 되지?").
- 본문: 강의에서 다루는 핵심 노하우를 직접 가르치지 말고, '이런 게 있다'는 존재만 알려준다 (예: "이 한 가지만 알면 달라지는데… 그게 뭔지는 강의에서 직접 보여드릴게요").
- 질문 1~2개로 독자의 궁금증을 건드린다 (예: "혹시 ○○할 때 왜 그렇게 되는지 아세요?").
- 완전 공개 금지: 구체적 수치, 단계별 방법, 해결책을 직접 쓰지 않는다.
- 마무리: 링크 클릭 또는 수강을 유도하는 CTA.
- 해시태그 10~15개(#소잉 #홈소잉 #자야쌤 + 이번 주제 태그).

[작업] 위 [강의 자막]을 바탕으로 인스타 피드 캡션을 쓴다.
**해시태그 제외 본문은 1,000~1,300자 내외**로 맞춘다. 캡션 전문만 출력한다.
${topicLine(topic)}`;
}

// ─────────────────────────────────────────────
// 수정(revise) — 카페 서머리 / 인스타 캡션만
// ─────────────────────────────────────────────
export function buildRevisePrompt({ type, original, instruction, voice }) {
  const label = TYPE_LABELS[type];
  const rules =
    type === 'cafe'
      ? '**글자수는 2,000자 내외**를 유지한다.'
      : '**해시태그 제외 본문은 1,000~1,300자 내외**를 유지하고 해시태그 10~15개를 포함한다.';
  return `${voiceBlock(voice)}

[티저 원칙 유지] 수정 후에도 강의 내용을 직접 설명하지 않는다. 독자가 수강 욕구를 느끼도록 궁금증·공감을 자극하는 방향을 유지한다.

[작업] 아래는 위 [강의 자막]을 바탕으로 이미 작성한 ${label} 초안이다.
[수정 지시]에 따라 해당 부분만 고치고, 나머지 톤과 내용은 유지해 전체를 다시 쓴다.
자막에 없는 사실은 추가하지 않는다. ${rules}
수정된 ${label} 전문만 출력한다(설명·머리말 없이).

[기존 ${label}]
${original}

[수정 지시]
${instruction}`;
}

// ─────────────────────────────────────────────
// 재생성(regenerate) — 확정된 카페·캡션 기반으로 캐러셀·캡쳐 재생성
// ─────────────────────────────────────────────
export function buildRegeneratePrompt({ type, cafe, caption, topic }) {
  const context = `[확정된 카페 서머리]
${cafe}

[확정된 인스타 캡션]
${caption}`;

  if (type === 'carousel') {
    return `[컨텍스트] 아래는 이 강의에 대해 확정된 콘텐츠다. 캐러셀은 이 내용과 메시지·표현이 일관되어야 한다.
${context}

${buildCarouselPrompt({ topic })}`;
  }
  if (type === 'capture') {
    return `[컨텍스트] 아래는 이 강의에 대해 확정된 콘텐츠다. 캡쳐 시점은 이 내용에서 강조된 장면을 우선 반영한다.
${context}

${buildCapturePrompt()}`;
  }
  throw new Error(`프롬프트 조립: 재생성 대상이 아닌 타입입니다 — ${type}`);
}

export { TYPE_LABELS };
