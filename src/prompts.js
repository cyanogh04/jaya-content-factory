/** 공통 헤더 — 모든 생성 호출의 system 프롬프트 (동일 유지 = 프롬프트 캐시 프리픽스 보존) */
export const COMMON_HEADER = `당신은 소잉 강사 '자야쌤'의 콘텐츠 작가다.
[철학] "옷을 만드는 게 아니라 짓는다." 왜 그렇게 하는지까지 담는다.
[대상] 제대로 만들고 싶은 30~50대 취미 소잉러.
[원칙] 자막에 없는 사실은 지어내지 않는다.`;

const TYPE_LABELS = {
  cafe: '네이버 카페 서머리',
  caption: '인스타 캡션',
  carousel: '인스타 캐러셀 기획',
  capture: '캡쳐 가이드',
};

function topicLine(topic) {
  return topic ? `[강의 주제] ${topic}\n` : '';
}

/** 보이스 프로파일 JSON → 프롬프트용 요약 문자열 (대표 캡션은 EXAMPLES로 분리) */
function voiceSummary(voice) {
  const { representative_captions, ...rest } = voice || {};
  return JSON.stringify(rest, null, 2);
}

/** 대표 캡션 원문 3~5개 */
function voiceExamples(voice) {
  const caps = (voice?.representative_captions || []).slice(0, 5);
  if (caps.length === 0) return '(대표 캡션 없음)';
  return caps.map((c, i) => `--- 예시 ${i + 1} ---\n${c}`).join('\n\n');
}

/** 말투 지침 블록 (cafe·caption 공용) */
function voiceBlock(voice) {
  return `[말투 지침] 아래 보이스 프로파일과 예시 캡션의 목소리를 그대로 재현한다.
보이스:
${voiceSummary(voice)}

예시 캡션:
${voiceExamples(voice)}`;
}

/**
 * 노출 수위 규칙 — 카페·캡션·캐러셀 공용.
 * "얼마나 줄까"가 아니라 "어떤 종류를 줄까"로 선을 긋는다.
 * 진단(왜 그런가)은 전부 주고, 처방(어떻게 하는가)만 강의에 남긴다.
 * 금지 목록은 모델이 스스로 검사할 수 있도록 형태(수치·순서·명령형·위치)로 정의한다.
 */
const DISCLOSURE_RULE = `[노출 수위 — 후킹과 강의 보호를 동시에 잡는 기준]
원칙: **'왜'는 아낌없이 주고, '어떻게'는 이름만 준다.**
진단(무엇이 문제이고 왜 그런가)은 다 줄수록 "이 사람 진짜 아는구나"가 증명돼 후킹이 세진다.
처방(그래서 어떻게 하는가)만 강의에 남긴다. 정보를 통째로 아끼면 후킹도 신뢰도 같이 잃는다.
가장 나쁜 건 얼버무리는 글이다 — "자세한 건 강의에서 알려드릴게요"로 넘어가지 말고, 진단은 끝까지 선명하게 쓴다.

▸ 아낌없이 주는 것 (진단)
- 오답 지우기: 독자가 그동안 시도해온 방법이 왜 소용없었는지 짚는다. ("바늘을 바꿔봐도 안 돼요")
- 진짜 원인 지목: 문제의 출처를 정확히 짚는다. ("문제는 박는 힘이 아니라 시작점이에요")
- 해법에 이름·개수 붙이기: ("기준이 되는 칸이 따로 있어요", "원인은 딱 세 가지인데 2번은 다들 모르세요")
  → 이름이 있으면 해법이 실재한다는 게 증명되고, 이름만으로는 따라 할 수 없어 강의는 지켜진다.
- 대가 밝히기: 그 답을 얻기까지 치른 값. ("저는 원단 세 마 버리고 알았어요")
- 전후 대비: 망한 결과와 제대로 된 결과를 나란히 보여준다 — 결과만, 방법 설명 없이.

▸ 주지 않는 것 (강의 몫). 아래가 본문에 나오면 선을 넘은 것이다.
- 수치·규격 (몇 mm, 몇 도, 장력 얼마, 시접 몇 cm)
- 순서·단계 ("먼저 ~하고 그다음 ~한다")
- 실행 지시형 문장 ("~하세요", "~해서 박으세요")
- 기준점의 위치·판별법 (기준이 있다는 건 말하되, 그게 어디인지·어떻게 찾는지는 말하지 않는다)

▸ 자가 검증: "이것만 보고 독자가 실제로 따라 할 수 있는가?"
따라 할 수 있으면 너무 준 것이니 처방을 걷어낸다. 따라 하고 싶은데 못 하면 정확히 맞은 것이다.`;

