// 1. 말투 분석 → 보이스 프로파일 (learn-voice)
// ─────────────────────────────────────────────
export function buildVoicePrompt(captions) {
  const joined = captions.map((c, i) => `--- 캡션 ${i + 1} ---\n${c}`).join('\n\n');
  return `아래는 자야쌤이 직접 쓴 인스타그램 캡션 모음이다. 이 사람의 문체를 분석해
다른 글도 같은 목소리로 쓸 수 있도록 '보이스 프로파일'을 만들어라.

반드시 아래 JSON 형식으로만 출력한다(설명·머리말 없이):
{
  "tone": "전반적 어조 2~3문장",
  "sentence_style": "문장 길이·리듬·끝맺음 패턴",
  "signature_expressions": ["자주 쓰는 표현/어미/입버릇 5~10개"],
  "punctuation_emoji": "말줄임표·기호·이모지 사용 습관",
  "hook_patterns": ["첫 문장을 여는 방식 3~5개"],
  "avoid": ["이 사람이 쓰지 않을 표현/톤"],
  "representative_captions": ["가장 대표적인 원문 캡션 3~5개 그대로"]
}

[캡션 모음]
${joined}`;
}

